function hoverOn(element) {
	if (element.id.charAt(0) == 'd')
	{
		theID = element.id.substr(1, element.id.length - 1);
	} else
	{
		theID = element.id;
	}
	if (document.getElementById(theID).className == 'trait')
	{
		document.getElementById(theID).className = 'traitHighlight';
		document.getElementById('d' + theID).className = 'descriptionHighlight';
	}
	document.getElementById(theID).style.cursor= 'pointer';
	document.getElementById('d' + theID).style.cursor= 'pointer';
}

function hoverOff(element) {
	if (element.id.charAt(0) == 'd')
	{
		theID = element.id.substr(1, element.id.length - 1);
	} else
	{
		theID = element.id;
	}
	if (document.getElementById(theID).className == 'traitHighlight')
	{
		document.getElementById(theID).className = 'trait';
		document.getElementById('d' + theID).className = 'description';
	}
	document.getElementById(theID).style.cursor= 'auto';
	document.getElementById('d' + theID).style.cursor= 'auto';
}

function toggle(element) {
	if (element.id.charAt(0) == 'd')
	{
		theID = element.id.substr(1, element.id.length - 1);
	} else
	{
		theID = element.id;
	}
	var j = theID * 1;
	if (Trait[j] == 0)
	{
		Trait[j] = 1;
		document.getElementById(theID).className = 'traitSelect';
		document.getElementById('d' + theID).className = 'descriptionSelect';
		if (j%2)
		{
			j--;
		} else
		{
			j++;
		}
		if (Trait[j] == 1)
		{
			Trait[j] = 0;
			document.getElementById(j).className = 'trait';
			document.getElementById('d' + j).className = 'description';
		}
	}
	else
	{
		Trait[j] = 0;
		document.getElementById(theID).className = 'traitHighlight';
		document.getElementById('d' + theID).className = 'descriptionHighlight';
	}
	updateSTTable();
}

